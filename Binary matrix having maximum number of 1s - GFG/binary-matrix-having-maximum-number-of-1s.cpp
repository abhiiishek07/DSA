class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> mat, int N) {
        //code here
            int row=0,cnt=0;
            int i=0,j=N-1;
            while(i<N && j>=0){
                if(mat[i][j]){
                    row=i;
                    cnt=N-j;
                    j--;
                }
                else i++;
            }
            return {row,cnt};
        
    }
};

