class Solution {
  public:
    vector<int> getDistinctDifference(int N, vector<int> &A) {
        // code here
        unordered_set<int>left,right;
        vector<int>ans,l,r;
        for(int i=0;i<N;i++){
            l.push_back(left.size());
            left.insert(A[i]);
            
            r.push_back(right.size());
            right.insert(A[N-i-1]);
        }
        reverse(r.begin(),r.end());
        for(int i=0;i<N;i++){
            ans.push_back(l[i]-r[i]);
        }
        return ans;
        
    }
};


