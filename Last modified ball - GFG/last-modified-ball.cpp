class Solution {
  public:
    int solve(int N, vector<int> A) {
        // code here
        int i=N-1,carry=1;
        while(i>=0){
            int s= A[i]+carry;
            if(s<=9) break;
            carry= s/10;
            i--;
        }
        return i+1;
    }
};

